    private void excluir() {
        int linha = tabelaContato.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tabelaContato.getModel();
        model.removeRow(linha);
    }

    private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarActionPerformed
        CursoEditor editor = new CursoEditor();
        editor.setVisible(true);
        int linha = tabelaContato.getSelectedRow();

        int l = ++linha;
        Curso curso = mapCurso.get(l);
        mapCurso.remove(l);
        editor.editar(curso, this);
        Curso contatoNovo = new Curso();
        contatoNovo.setNome(this.curso.getNome());
        contatoNovo.setEmenta(this.curso.getEmenta());

        if (curso != this.curso) {
            mapCurso.put(l, this.curso);
        }

        excluir();
    }//GEN-LAST:event_botaoEditarActionPerformed


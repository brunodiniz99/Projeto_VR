    private void populaTb() {
        DefaultTableModel model = (DefaultTableModel) tabelaContato.getModel();
        mapCurso.put(numeroSequencia++, curso);
        model.addRow(new String[]{
            curso.getNome(), curso.getEmenta()});
    }
    
    private void botaoOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOKActionPerformed
        
        if(textNome.getText().isEmpty() && textSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite o nome e a senha do aluno!");
            return;
        }
        
        Aluno aluno = new Aluno();
        
        aluno.setNome(textNome.getText());
        aluno.setSenha(textSenha.getText());

        if (mapCurso != null) {
            List<Curso> cursos = new ArrayList<>();
            for (Map.Entry<Integer, Curso> entry : mapCurso.entrySet()) {
                cursos.add(entry.getValue());
            }

            aluno.setCursos(cursos);
            CursoDao cursoDao = new CursoDao();
            cursoDao.salvar(curso);
        }
        alunoDao.salvar(aluno);
        
        this.dispose();
        
    }//GEN-LAST:event_botaoOKActionPerformed

